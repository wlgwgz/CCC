const U08 * italian_uncompress_text(U16 idx)
{
    U16 bytes = italian_bytes[idx];
    const U08* ptr = &str_bin[get_italian_idx_ofs(idx)];
    U16 n=0;
    U16 i=0;
    while (n<bytes && i+2+2 <= MAX_STRING_BYTES)
    {
        U16 uncompress_code = italian_uncompress_tab[ptr[n]];
        uncompress_buf[i++] = (uncompress_code>>0)&0xFF;
        uncompress_buf[i++] = (uncompress_code>>8)&0xFF;
        n++;
    }
    uncompress_buf[i++] = ((ITALIAN_END_CHAR) >> 0)&0xFF;
    uncompress_buf[i++] = ((ITALIAN_END_CHAR) >> 8)&0xFF;
    return (const U08 *)&uncompress_buf[0];
}

