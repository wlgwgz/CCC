U08 uncompress_buf[MAX_STRING_BYTES];
const U08 * chinese_uncompress_text(U16 idx)
{
    U16 bytes = chinese_bytes[idx];
    const U08* ptr = &str_bin[get_chinese_idx_ofs(idx)];
    U16 n=0;
    U16 i=0;
    while (n<bytes && i+1+2 <= MAX_STRING_BYTES)
    {
        uncompress_buf[i++] = ptr[n++];
    }
    uncompress_buf[i++] = ((CHINESE_END_CHAR) >> 0)&0xFF;
    uncompress_buf[i++] = ((CHINESE_END_CHAR) >> 8)&0xFF;
    return (const U08 *)&uncompress_buf[0];
}

